def countVowels (s):
    count = 0
    for character in s:
        if character == ('a'):
             count += 1
        elif character == ('e'):
             count += 1
        elif character == ('i'):
             count += 1
        elif character == ('o'):
             count += 1
        elif character == ('u'):
             count += 1
        elif character == ('A'):
             count += 1
        elif character == ('E'):
             count += 1
        elif character == ('I'):
             count += 1
        elif character == ('O'):
             count += 1
        elif character == ('U'):
             count += 1
    print ('Number of vowels:', count)
